                            Desse total, excluídos os casos de indeferimento ou arquivamento de
                            requerimento, bem como de extinção de contrato, tem-se 71 (setenta e um)
                            pedidos em vigor, dos quais foram celebrados e se encontram vigentes 46
                            (quarenta e seis) contratos de adesão.
                        </p>
