                    <p class="callout" style="text-align: justify;" data-mce-style="text-align: justify;">
                        <strong>Dados dos Requerimentos</strong>
                    </p>
                    <p>
                        Desde o estabelecimento do instituto de outorga por autorização
                        ferroviária, ocorreram 106 (cento e seis) requerimentos, oriundos de 52
                        (cinquenta e dois) diferentes entes privados.
                    </p>
                    <p>
                        Desse total, excluídos os casos de indeferimento ou arquivamento de
                        requerimento, bem como de extinção de contrato, tem-se 71 (setenta e um)
                        pedidos em vigor, dos quais foram celebrados e se encontram vigentes 46
                        (quarenta e seis) contratos de adesão.
                    </p>
                    <p>
                        A projeção de recursos privados a serem alocados na implantação dos
                        empreendimentos já outorgados somam, aproximadamente, R$ 225 bilhões e
                        são previstos 12 mil quilômetros de novas ferrovias.
